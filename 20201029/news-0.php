                                </div><br>
                                  <p>这就是北京世宁律师事务所的开创者们，永远直面行业最前沿的复杂纠纷，解决最棘手的法律难题，与最先进的科学技术、生产方式及商业模式相伴相行，在法律和商业的历练中找寻人生的目标。</p>
                                  <p>最终，百川入海，这些在各自领域深耕多年、都做得非常优秀的人，被共同的理念所吸引，走到了一起，汇入了世宁。</p>
								  <br>
                                <h5>二、我们要做感动他人、感动自己的产品</h5>
								  <br>
								  <p>不少人问过周云川一个问题：“你能说说你认为最好的几个知识产权诉讼律师吗？”周云川觉得自己的回答不尽如人意。走出体制后的这几年，周云川发现优质的法律服务供给和需求之间还有不小的缺口，“争议解决法律服务，是一门学问，有其内在规律，在这方面还有很大的空间。世宁应该在这方面做文章。”</p>
								  <p>越在行业浸淫，世宁的创办者们就越感到精细化分工是未来的大势所趋。知识产权和竞争法领域是风云集散之地，糅合了高精尖技术、法律与商业的较量。跨国、跨域争议的愈加频繁意味着国际化人才愈加稀缺。想要将知识产权和反垄断做好做精，回应客户日益复杂化、个性化、行业化的法律需求，应当有背景多元、经验丰富、功底深厚、优势互补的精英团队。</p>
								  <p>“我们的核心竞争力就是这些人，以这些人的知识和经验为基础，加上制度、流程、态度、文化价值的保障。”“最终所呈现在客户面前的，是我们能够感动人的产品。”刘永全如此说。而要提供能够感动他人的服务，世宁的决心是首先要感动自己。</p>
								  <p>为此，世宁践行一体化工作模式。案件承接后，所内根据需求组建最适合的律师团队，以做好事情为最高优先级，兼顾效率与成本，为客户提供专业高效的卓越服务。</p>
								  <p> “传统的律所管理模式是‘个体户’制，但真正要发挥团队的力量、解决最棘手的问题、承接最复杂的项目，就必须借鉴国际先进的企业运营经验。”陈文煊描述，“在世宁模式下，既有全所统一的专人负责项目流程管理、期限管理、知识库管理、法规案例库管理、CRM系统管理，又有项目经理、承办律师和项目组成员基于各自细致分工基础上的任务流管理，还有标准化、系统化的知识、能力和组织升级规划。”</p>
								   <div class="l_news_item"><br>
                                  <img class="img-fluid" src="foto/news/001.jpg" alt="logo"><br>
                                </div><br>
								  <p>世宁的创办者们也深刻认识到，要提供好的服务产品，离不开人才的培养。“世宁的年轻人是我们最大的财富，我们无比珍惜每一个将未来交付给我们的律师”，冯斐斐表示，“为此我们不计成本，尽最大的努力，帮助他们成长。” 世宁渴求优秀、有潜力的好苗子，在他们加入世宁后，根据每一位成员的自身情况与意愿，量身定制人才的能力进阶计划。</p>
								  <p>除了法律专业技能，世宁亦格外注重商业思维、科技知识、管理能力的培养。世宁律师始终保持对外部世界的好奇心和敏感度，这包括全局视觉、商业味觉、风险嗅觉、创新感觉、管理触觉。</p>
							    <p>世宁不仅打造内部精英团队，还借助外部力量整合资源。与优质的外部技术专家、检测及鉴定机构、调查咨询公司、公共关系公司等所进行的密切合作，使得世宁能够根据需要配置适宜资源，为客户赢取最佳法律成果及持续领先的商业竞争优势。</p>
								  <p>种种制度的背后，是为了更好地实现世宁的使命、愿景，成就客户，成就律所，成就所有的团队成员，最终服务于一个共同的目标——提供有价值、有温度的、能够打动人的法律服务。</p><br>
							      <br>
                                <h5>三、我们的白米饭文化 就是在日常的点滴中吃出甜味</h5>
								  <p>这是一个年轻的律所，也是年轻人的律所，只要有想法，都可以在这里充分地表达。这是一个一体化、扁平化的团队，律师们可以充分交流、合作，年轻的律师可以得到所有合伙人、基于不同的人生经验、来自不同角度的指导。</p>
								   <div class="l_news_item"><br>
                                  <img class="img-fluid" src="foto/news/002.jpg" alt="logo"><br>
                                </div><br>
								  <p>
								  “我们希望打造的是让最强大脑能够碰撞出思想火花的氛围，我们也期待能够最大程度地让年轻人大胆尝试、释放出创造力，实现个人成功和团队发展的统一。”李楠说道，“我们的文化就是这样，‘尊重、共享与共赢’”。</p>
								  <br>
								  <p>在世宁，每个人并不是流水线上的螺丝钉。由于世宁为客户所提供的服务极富个性化，工作也极富挑战性。即使是行政的工作，在世宁也能做出花来，处处都可以体现用心、品质和温度，大大拉进了世宁人彼此之间的距离。</p>
								  <p>每天中午，所里的行政会用最好的电饭锅、最好的米、最好的煮饭方法，统一煮一大锅香喷喷的白米饭，大家围坐在一起，谈天说地、纵横时事，其乐融融。</p>
								  <p>这就是世宁的“白米饭文化”：在日常的点滴中，我们共同吃出甜味——共甘苦、共成长、共担当、共奋斗。</p>
							    <p>这就是我们，当梦想照进现实，厚积薄发的世宁，永远行走在路上。</p>
							    <p>世宁的信息窗上 看看我们的律师如何说</p>
							    <p>——“这里有最牛的领导，有最佳的队友，有诸多做复杂疑难案子的机会。在这里，菜鸟可以快速成长，老手也能够更上层楼。”</p>
							    <p>——“世宁没有世故圆滑，带着青涩，就这样敞开情怀，接纳着每一位‘才子佳人’。”</p>
							    <p>——“大家的业务能力都很棒，对公共事务的参与度也很高，在这里工作有一种融入感和归属感——THAT'S MY OFFICE，同时办公室氛围非常融洽，比如中午会提供优质大米饭，聚餐时大家会分享生活智慧等等——家的感觉。”</p>
							    <p>——“在世宁，大家乐于分享和沟通，互相理解和鼓励，在心灵愉悦和感恩的氛围中，每个人都努力工作，满满的正向循环，很喜欢这种氛围并为之吸引。”</p>
							    <p>——“世宁是一个由资深律师组成的年轻团队，正如三伏天的朝阳，热烈而富有生机。”</p>
							    <p>——“一群心有浩然的人在扁平化的管理中不断地迭代成长，把大律师的梦想落地的地方。”</p>
							    <p>——“在这里真的实现了自我驱动这件事，它给了我很多的安全感，只消全力去追、敢想敢做，因为背后有坚定且强大的支撑。”</p>
