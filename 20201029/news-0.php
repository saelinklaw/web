                              <div class="static_text2">
								  <p>世宁的目标是发展成为高端知识产权、竞争法和商事诉讼顶尖律师事务所之一。这一目标的实现靠的是吸引及留住业内最杰出的专业人才，为他们提供迅速成长、充分发挥才干的平台。</p><br>
                                  <h5>一、我们都有颗追求梦想的心</h5>
                                  <br>
                                <p>当24年前周云川从福建到北京上大学时，怎么也想不到，在读了7年公安大学后，会进入法院工作了15年，做了12年知识产权法官。更让人想不到的是他会在40岁“高龄”自费去美国加州大学伯克利法学院读书，而后辞去公职，进入一家国际律所香港办公室开始另一段不同的人生道路。不少朋友诧异于他的选择，但他却说“这些都是经历，都是积累，看似偶然，其实有必然。会走出这么一条路，根本上是源自一颗不安分、追求梦想的心。</p>
                                  </p><br>
                                 <div class="l_news_item">
                                  <img class="img-fluid" src="foto/news/yczhou.jpg" alt="logo"> <br>
                                </div><br>
                                <p>同样“不安分”的还有刘永全，本该在科研道路上一路前行的他，在取得生化博士学位和做了十余年药物研发之后，却自学并高分通过了专利代理师资格考试和国家司法考试，转而投身专利诉讼的事业。十余年来，他代理的案件从生化领域不断扩展到机械、芯片和通信领域，从地方法院一直到最高法院。长期浸淫于多个不同领域的专利技术，他练就了将复杂问题简单化，简单问题逻辑化的深厚功力。在最高人民法院历年公布的全国“十大”、“五十大”典型知识产权案例中，他本人代理的案件数就超过了中国绝大多数的律所。</p>
                                  <div class="l_news_item"><br>
                                  <img class="img-fluid" src="foto/news/rliu.jpg" alt="logo"><br>
                                </div><br>
                                  <p>陈文煊的人生关键词是“挑战”——曾经拿过全国生物和物理竞赛奖的理科生，高考时毅然将人民大学法学专业列为了第一志愿；曾经深入研究过“股权分置改革”的商法研究生，进法院后却一头扎进了知识产权审判的沃土；曾经出版过专利法专著、笔耕不辍的审判长，却转身挑起了阿里巴巴这一全球最大电子商务经济体的知识产权保护和新型互联网争议解决的重任。在深耕中国多年后，他再次推开了人生的任意门，步入美国宾夕法尼亚大学法学院和沃顿商学院的殿堂，寻求本土经验和国际视野的融合。</p>
								  <div class="l_news_item"><br>
                                  <img class="img-fluid" src="foto/news/wxchen.jpg" alt="logo"><br>
                                </div><br>
                                 <p>与他们不同，冯斐斐走的是“根正苗红”的律师之路，北大毕业后即加入了“红圈所”，跟随国内第一梯队的诉讼团队一路成长，在大量重大商事诉讼的锤炼中，逐步练就了快速切入争议焦点、深入理解法律问题、审慎甄别细节等优秀律师的本领。七年后，她又远赴美国哥伦比亚大学深造，并于毕业后加入国际律所，专注于知识产权领域不断深耕。在处理系列最前沿、最复杂、跨地域的国际诉讼过程中，她磨砺出多角度思考、全盘统筹、处理棘手问题的宝贵经验。</p>
