                                  <p>同样理工科出身、转行到知识产权领域的还有李楠，她以北京市宣武区高考状元的成绩考入清华大学自动化系，又在中科院计算机应用专业取得了硕士学位。走出知识的象牙塔后，李楠投身于国家知识产权局专利审查工作，一干就是十几年。作为北京知识产权法院首批技术型人民陪审员和北京知识产权局首批技术分析师，她直接参与了众多涉及专利、商业秘密、软件著作权等专业性极强的重大疑难复杂的大案、要案的技术调查和分析工作。</p>
